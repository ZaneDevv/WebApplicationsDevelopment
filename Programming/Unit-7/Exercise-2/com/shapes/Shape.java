package com.shapes;


/**
 * Interface for shapes
 * @version 1.0
 * @author Álvaro Fernández Barrero
 */
public abstract class Shape
{
    /**
     * Computes the current shape's area
     * @return The current shape's area
     * @version 1.0
     * @since 1.0
     * @author Álvaro Fernández Barrero
     */
    public abstract float computeArea();
}